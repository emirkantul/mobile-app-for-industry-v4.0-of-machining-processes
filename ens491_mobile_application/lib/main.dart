List<FlSpot>getDataAsSpots() {
  List<FlSpot> result = [];
  for (var i = 0; i < data.length; i++) {
    var x = data[i]["time"];
    var y = data[i]["vibration"];
    result.add(FlSpot(x!, y!));
  }
  return result;
}

